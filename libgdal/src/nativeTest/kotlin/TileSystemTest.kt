    @Test
    fun zoomToScaleAtEquatorZoom() {
        listOf(
            0.0 to 591_658_710.91,
            1.0 to 295_829_355.45,
            2.0 to 147_914_677.73,
            3.0 to 73_957_338.86,
            4.0 to 36_978_669.43,
            5.0 to 18_489_334.72,
            6.0 to 9_244_667.36,
            7.0 to 4_622_333.68,
            8.0 to 2_311_166.84,
            9.0 to 1_155_583.42,
            10.0 to 577_791.71,
            11.0 to 288_895.85,
            12.0 to 144_447.93,
            13.0 to 72_223.96,
            14.0 to 36_111.98,
            15.0 to 18_055.99,
            16.0 to 9_028.0,
            17.0 to 4_514.0,
            18.0 to 2_257.0,
            19.0 to 1_128.5,
            20.0 to 564.25,
            21.0 to 282.12,
            22.0 to 141.06,
            23.0 to 70.53,
        ).forEach {
            val expectedZoom = it.first
            val expectedScale = it.second
            val scale = subject.zoomToScale(expectedZoom, 0.0)
            val zoom = subject.scaleToZoom(expectedScale, 0.0)
            assertTrue(abs(scale - expectedScale) / expectedScale < 1e-4,
                "zoom $expectedZoom: expected scale $expectedScale but got $scale")
            assertTrue(abs(zoom - expectedZoom) < 0.001,
                "scale $expectedScale: expected zoom $expectedZoom but got $zoom")
        }
    }

    @Test
    fun scaleZoomRoundTrip() {
        val pairs = listOf(
            Pair(5.0, 0.0),
            Pair(10.0, 45.0),
            Pair(14.0, 47.6),
            Pair(18.0, -33.9),
        )
        for ((z, lat) in pairs) {
            val roundTrip = subject.scaleToZoom(subject.zoomToScale(z, lat), lat)
            assertTrue(abs(roundTrip - z) < 0.0001, "Round-trip failed for z=$z lat=$lat: got $roundTrip")
        }
    }

    @Test
    fun scaleToZoomSeattle() {
        // At Seattle latitude (~47.6°), 1:35,000 should be around street level (zoom ~14)
        val zoom = subject.scaleToZoom(35_000.0, 47.6)
        assertTrue(zoom > 13.0 && zoom < 14.0, "Expected street-level zoom (~14) but got $zoom")
    }

    @Test
    fun createTileClipPolygon() {
        val z = 16
        val x = 10485
        val y = 22975
        val polygon = subject.createTileClipPolygon(x, y, z)
        val bounds = polygon.envelope()
        assertEquals(47.28295557691229, bounds.north)
        assertEquals(-122.4041748046875, bounds.west)
        assertEquals(47.27922900257082, bounds.south)
        assertEquals(-122.398681640625, bounds.east)
    }
